/// One entry in the controller's metadata-rich queue.
#[derive(Debug, Clone)]
struct QueueEntry {
    id: QueueId,
    kind: QueueKind,
    preview: String,
    queued_at_ms: u64,
    message: Message,
}

impl QueueEntry {
    fn to_public(&self) -> QueuedPrompt {
        QueuedPrompt {
            id: self.id.clone(),
            kind: self.kind,
            preview: self.preview.clone(),
            queued_at_ms: self.queued_at_ms,
        }
    }
}

/// Subscriber callback for queue snapshots.
type SnapshotSubscriber = Arc<dyn Fn(QueueSnapshot) + Send + Sync + 'static>;

/// The controller-owned queue store. Guarded by a sync mutex and never held
/// across an `.await`.
struct QueueStore {
    /// Ordered queue. Steering and follow-up are interleaved here for stable
    /// public ordering / snapshots, but drained into the agent by kind.
    entries: Vec<QueueEntry>,
    /// Monotonic id counter shared with the controller.
    snapshot_subscribers: Vec<(u64, SnapshotSubscriber)>,
    next_sub_id: u64,
    session_id: Option<String>,
}

impl QueueStore {
    fn new(session_id: Option<String>) -> Self {
        Self {
            entries: Vec::new(),
            snapshot_subscribers: Vec::new(),
            next_sub_id: 0,
            session_id,
        }
    }

    fn public_queued(&self) -> Vec<QueuedPrompt> {
        self.entries.iter().map(QueueEntry::to_public).collect()
    }

    fn snapshot(&self, active: bool) -> QueueSnapshot {
        QueueSnapshot {
            session_id: self.session_id.clone(),
            active,
            queued: self.public_queued(),
        }
    }

    /// Remove and return all entries of a kind, in FIFO order (used for `all`
    /// drain mode at the controller layer — actual mode is governed by the
    /// agent's queue, so the controller hands every matching entry to the agent
    /// queue and lets the agent's mode decide pop behavior).
    fn drain_kind(&mut self, kind: QueueKind) -> Vec<Message> {
        let mut drained = Vec::new();
        let mut remaining = Vec::with_capacity(self.entries.len());
        for entry in std::mem::take(&mut self.entries) {
            if entry.kind == kind {
                drained.push(entry.message);
            } else {
                remaining.push(entry);
            }
        }
        self.entries = remaining;
        drained
    }

    /// Pop the single oldest entry of a kind (one-at-a-time controller drain).
    fn pop_one_kind(&mut self, kind: QueueKind) -> Option<Message> {
        let pos = self.entries.iter().position(|e| e.kind == kind)?;
        Some(self.entries.remove(pos).message)
    }

    fn cancel(&mut self, id: &QueueId) -> bool {
        if let Some(pos) = self.entries.iter().position(|e| &e.id == id) {
            self.entries.remove(pos);
            true
        } else {
            false
        }
    }

    /// Promote a queued follow-up to steering in place, preserving its message
    /// (including images) and id. Returns `true` if found and was a follow-up.
    fn promote_follow_up_to_steering(&mut self, id: &QueueId) -> bool {
        if let Some(entry) = self.entries.iter_mut().find(|e| &e.id == id) {
            if entry.kind == QueueKind::FollowUp {
                entry.kind = QueueKind::Steering;
                return true;
            }
        }
        false
    }

    /// Clear queued entries with control over which kinds are removed.
    /// Returns the number of entries removed.
    fn clear(&mut self, clear_steering: bool, clear_follow_up: bool) -> usize {
        let before = self.entries.len();
        self.entries.retain(|e| match e.kind {
            QueueKind::Steering => !clear_steering,
            QueueKind::FollowUp => !clear_follow_up,
        });
        before - self.entries.len()
    }
}

