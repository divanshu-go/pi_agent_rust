// Idle signaling
// ============================================================================

/// Tracks active-turn state and wakes `wait_for_idle` waiters on completion.
struct IdleState {
    active: AtomicBool,
    waiters: StdMutex<Vec<oneshot::Sender<()>>>,
}

impl IdleState {
    const fn new() -> Self {
        Self {
            active: AtomicBool::new(false),
            waiters: StdMutex::new(Vec::new()),
        }
    }

    fn is_active(&self) -> bool {
        self.active.load(Ordering::SeqCst)
    }

    fn mark_active(&self) {
        self.active.store(true, Ordering::SeqCst);
    }

    /// Mark idle and wake all current waiters.
    fn mark_idle(&self) {
        self.active.store(false, Ordering::SeqCst);
        if let Ok(mut waiters) = self.waiters.lock() {
            for tx in waiters.drain(..) {
                let _ = tx.send(());
            }
        }
    }

    /// Returns a receiver that resolves when the turn becomes idle, or `None`
    /// if already idle.
    fn subscribe_idle(&self) -> Option<oneshot::Receiver<()>> {
        if !self.is_active() {
            return None;
        }
        let (tx, rx) = oneshot::channel();
        if let Ok(mut waiters) = self.waiters.lock() {
            // Re-check under the lock to avoid a race where the turn finished
            // between the is_active() check and acquiring the lock.
            if !self.active.load(Ordering::SeqCst) {
                return None;
            }
            waiters.push(tx);
            Some(rx)
        } else {
            None
        }
    }
}

/// Marks the controller idle on drop — including during panic unwinding of a
/// spawned turn. Without this, a panic inside `run_turn` (before its own
/// `mark_idle`) would leave the controller permanently "active", hanging every
/// future `wait_for_idle`. Parity gap H5: embedded has no subprocess to crash,
/// so a poisoned turn must not poison the controller.
struct IdleGuard {
    idle: Arc<IdleState>,
}

impl Drop for IdleGuard {
    fn drop(&mut self) {
        self.idle.mark_idle();
    }
}

