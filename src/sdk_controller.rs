impl AgentSessionController {
    /// Emit a fresh snapshot to subscribers reflecting current queue + active.
    fn emit_snapshot(&self) {
        let active = self.inner.idle.is_active();
        if let Ok(store) = self.inner.queue.lock() {
            let snap = store.snapshot(active);
            notify_subscribers(&store, &snap);
        }
    }

    /// Subscribe to every [`AgentEvent`] across all prompt turns. The listener
    /// fires for the lifetime of the controller (there is no unsubscribe at the
    /// controller layer; drop the controller to tear down).
    ///
    /// Acquires the session lock briefly to register; call during setup before
    /// the first prompt to avoid contending with an in-flight turn.
    pub async fn subscribe_events(&self, listener: impl Fn(AgentEvent) + Send + Sync + 'static) {
        let cx = AgentCx::for_current_or_request();
        if let Ok(guard) = OwnedMutexGuard::lock(Arc::clone(&self.inner.handle), &cx).await {
            guard.subscribe(listener);
        }
    }

    /// Current queue snapshot (cheap clone).
    #[must_use]
    pub fn queue_snapshot(&self) -> QueueSnapshot {
        let active = self.inner.idle.is_active();
        self.inner.queue.lock().map_or_else(
            |_| QueueSnapshot {
                session_id: None,
                active,
                queued: Vec::new(),
            },
            |store| store.snapshot(active),
        )
    }

    /// Subscribe to queue snapshots. The callback fires on every enqueue,
    /// cancel, promote, abort, and on turn idle. Returns a subscription that
    /// removes the listener when dropped.
    pub fn subscribe_queue(
        &self,
        callback: impl Fn(QueueSnapshot) + Send + Sync + 'static,
    ) -> QueueSubscription {
        let cb: SnapshotSubscriber = Arc::new(callback);
        let id = {
            let Ok(mut store) = self.inner.queue.lock() else {
                return QueueSubscription {
                    queue: Arc::clone(&self.inner.queue),
                    id: u64::MAX,
                };
            };
            let id = store.next_sub_id;
            store.next_sub_id = store.next_sub_id.saturating_add(1);
            store.snapshot_subscribers.push((id, cb));
            id
        };
        QueueSubscription {
            queue: Arc::clone(&self.inner.queue),
            id,
        }
    }

    /// Whether a prompt turn is currently active.
    #[must_use]
    pub fn is_active(&self) -> bool {
        self.inner.idle.is_active()
    }

    /// Resolve immediately when idle, otherwise after the active turn reaches
    /// its terminal `AgentEnd` event. Does not poll or sleep.
    pub async fn wait_for_idle(&self) -> Result<()> {
        if let Some(rx) = self.inner.idle.subscribe_idle() {
            // If the sender is dropped without firing, treat as idle.
            let _ = rx.await;
        }
        Ok(())
    }
}

impl AgentSessionController {
    /// Start a prompt turn. Rejects with a busy error if a turn is active.
    ///
    /// ACKs immediately by returning a [`PromptTicket`]; the turn runs in the
    /// background on the controller's spawner. Observe completion via
    /// [`Self::wait_for_idle`] or an event subscription.
    pub fn prompt(
        &self,
        input: impl Into<String>,
        images: Vec<ImageContent>,
    ) -> Result<PromptTicket> {
        if self.inner.idle.is_active() {
            return Err(Error::session(
                "Agent is currently streaming; queue a follow-up or steering message instead",
            ));
        }

        let text = input.into();
        let ticket = PromptTicket {
            id: self.next_ticket_id(),
        };

        // Mark active before spawning so a racing second prompt is rejected.
        self.inner.idle.mark_active();
        self.emit_snapshot();

        let handle = Arc::clone(&self.inner.handle);
        let abort_slot = Arc::clone(&self.inner.abort_slot);
        let idle = Arc::clone(&self.inner.idle);
        let queue = Arc::clone(&self.inner.queue);

        let fut: BoxFuture<'static, ()> = Box::pin(async move {
            // Guarantee idle is restored even if the turn panics (H5 isolation):
            // the guard's Drop runs during unwind, waking wait_for_idle waiters.
            let _idle_guard = IdleGuard {
                idle: Arc::clone(&idle),
            };
            run_turn(handle, abort_slot, idle, queue, text, images).await;
        });
        (self.inner.spawner)(fut);

        Ok(ticket)
    }

    /// Queue a follow-up message (delivered when the agent would go idle).
    /// Safe to call while a turn is active or idle.
    pub fn queue_follow_up(
        &self,
        input: impl Into<String>,
        images: Vec<ImageContent>,
        preview: Option<String>,
    ) -> Result<QueuedPrompt> {
        self.enqueue(QueueKind::FollowUp, input.into(), images, preview)
    }

    /// Queue a steering message (delivered at the next steering boundary).
    /// Safe to call while a turn is active or idle.
    pub fn queue_steering(
        &self,
        input: impl Into<String>,
        images: Vec<ImageContent>,
        preview: Option<String>,
    ) -> Result<QueuedPrompt> {
        self.enqueue(QueueKind::Steering, input.into(), images, preview)
    }

    fn enqueue(
        &self,
        kind: QueueKind,
        text: String,
        images: Vec<ImageContent>,
        preview: Option<String>,
    ) -> Result<QueuedPrompt> {
        let id = self.next_queue_id();
        let preview = make_preview(preview, &text);
        let message = build_user_message(text, images);
        let entry = QueueEntry {
            id,
            kind,
            preview,
            queued_at_ms: now_ms(),
            message,
        };
        let public = entry.to_public();
        {
            let mut store = self
                .inner
                .queue
                .lock()
                .map_err(|_| Error::session("queue lock poisoned"))?;
            store.entries.push(entry);
        }
        self.emit_snapshot();
        Ok(public)
    }

    /// Steer the active reply. While active, queues a steering message for the
    /// next steering boundary. While idle, starts a new turn (parity with RPC).
    pub fn steer(&self, input: impl Into<String>, images: Vec<ImageContent>) -> Result<()> {
        let text = input.into();
        if self.inner.idle.is_active() {
            self.enqueue(QueueKind::Steering, text, images, None)?;
            Ok(())
        } else {
            self.prompt(text, images).map(|_| ())
        }
    }

    /// Promote a queued follow-up to steering, preserving its message and
    /// images. Returns `false` if the id is missing or already in flight (i.e.
    /// no longer queued) or was not a follow-up.
    pub fn promote_queued_to_steer(&self, queue_id: &QueueId) -> Result<bool> {
        let promoted = {
            let mut store = self
                .inner
                .queue
                .lock()
                .map_err(|_| Error::session("queue lock poisoned"))?;
            store.promote_follow_up_to_steering(queue_id)
        };
        if promoted {
            self.emit_snapshot();
        }
        Ok(promoted)
    }

    /// Cancel a queued message before delivery. Returns `true` only if the
    /// item was still queued.
    pub fn cancel_queued(&self, queue_id: &QueueId) -> Result<bool> {
        let cancelled = {
            let mut store = self
                .inner
                .queue
                .lock()
                .map_err(|_| Error::session("queue lock poisoned"))?;
            store.cancel(queue_id)
        };
        if cancelled {
            self.emit_snapshot();
        }
        Ok(cancelled)
    }

    /// Abort the active turn and clear all queued steering + follow-up messages.
    /// Resolves after the active turn reaches its terminal state.
    pub async fn abort_all(&self) -> Result<AbortSummary> {
        let cleared = {
            let mut store = self
                .inner
                .queue
                .lock()
                .map_err(|_| Error::session("queue lock poisoned"))?;
            store.clear(true, true)
        };
        let aborted_active = self.fire_abort();
        self.emit_snapshot();
        self.wait_for_idle().await?;
        Ok(AbortSummary {
            aborted_active,
            cleared_queued: cleared,
        })
    }

    /// Abort the active turn but preserve queued follow-ups. Steering tied to
    /// the aborted turn is cleared; follow-ups remain queued.
    pub async fn abort_active_only(&self) -> Result<AbortSummary> {
        let cleared = {
            let mut store = self
                .inner
                .queue
                .lock()
                .map_err(|_| Error::session("queue lock poisoned"))?;
            store.clear(true, false)
        };
        let aborted_active = self.fire_abort();
        self.emit_snapshot();
        self.wait_for_idle().await?;
        Ok(AbortSummary {
            aborted_active,
            cleared_queued: cleared,
        })
    }

    /// Fire the active abort handle if present. Returns whether one was fired.
    fn fire_abort(&self) -> bool {
        let handle = self
            .inner
            .abort_slot
            .lock()
            .ok()
            .and_then(|guard| guard.clone());
        handle.is_some_and(|handle| {
            handle.abort();
            true
        })
    }

    /// Switch provider/model. Waits for any in-flight turn to reach idle, then
    /// mutates the session under its lock (serialized with prompt turns).
    pub async fn set_model(&self, provider: &str, model_id: &str) -> Result<()> {
        self.wait_for_idle().await?;
        let cx = AgentCx::for_current_or_request();
        let mut guard = OwnedMutexGuard::lock(Arc::clone(&self.inner.handle), &cx)
            .await
            .map_err(|e| Error::session(format!("session lock failed: {e}")))?;
        guard.set_model(provider, model_id).await
    }

    /// Current thinking level, if a session is configured. Reads under the
    /// session lock; safe to call while a turn is active.
    ///
    /// # Errors
    /// Returns an error if the session lock cannot be acquired.
    pub async fn thinking_level(&self) -> Result<Option<ThinkingLevel>> {
        let cx = AgentCx::for_current_or_request();
        let guard = OwnedMutexGuard::lock(Arc::clone(&self.inner.handle), &cx)
            .await
            .map_err(|e| Error::session(format!("session lock failed: {e}")))?;
        Ok(guard.thinking_level())
    }

    /// Set the thinking level. Waits for any in-flight turn to reach idle, then
    /// mutates the session under its lock (serialized with prompt turns), so the
    /// change applies to the next turn rather than mid-stream.
    ///
    /// # Errors
    /// Returns an error if waiting for idle fails, the session lock cannot be
    /// acquired, or the underlying handle rejects the level.
    pub async fn set_thinking_level(&self, level: ThinkingLevel) -> Result<()> {
        self.wait_for_idle().await?;
        let cx = AgentCx::for_current_or_request();
        let mut guard = OwnedMutexGuard::lock(Arc::clone(&self.inner.handle), &cx)
            .await
            .map_err(|e| Error::session(format!("session lock failed: {e}")))?;
        guard.set_thinking_level(level).await
    }

    /// Advance the thinking level one step (Off -> Minimal -> Low -> Medium ->
    /// High -> XHigh -> Off), matching the RPC `cycle_thinking_level` command.
    /// Returns the newly applied level.
    ///
    /// # Errors
    /// Returns an error if waiting for idle fails or the session lock cannot be
    /// acquired.
    pub async fn cycle_thinking_level(&self) -> Result<ThinkingLevel> {
        self.wait_for_idle().await?;
        let cx = AgentCx::for_current_or_request();
        let mut guard = OwnedMutexGuard::lock(Arc::clone(&self.inner.handle), &cx)
            .await
            .map_err(|e| Error::session(format!("session lock failed: {e}")))?;
        let next = match guard.thinking_level().unwrap_or_default() {
            ThinkingLevel::Off => ThinkingLevel::Minimal,
            ThinkingLevel::Minimal => ThinkingLevel::Low,
            ThinkingLevel::Low => ThinkingLevel::Medium,
            ThinkingLevel::Medium => ThinkingLevel::High,
            ThinkingLevel::High => ThinkingLevel::XHigh,
            ThinkingLevel::XHigh => ThinkingLevel::Off,
        };
        guard.set_thinking_level(next).await?;
        Ok(next)
    }

    /// Snapshot the current message history. Reads under the session lock.
    ///
    /// # Errors
    /// Returns an error if the session lock cannot be acquired or the handle
    /// fails to produce the history.
    pub async fn messages(&self) -> Result<Vec<Message>> {
        let cx = AgentCx::for_current_or_request();
        let guard = OwnedMutexGuard::lock(Arc::clone(&self.inner.handle), &cx)
            .await
            .map_err(|e| Error::session(format!("session lock failed: {e}")))?;
        guard.messages().await
    }

    /// Compact the conversation history now. Waits for idle, then runs
    /// compaction under the session lock. Compaction events are forwarded to
    /// the controller's event subscribers via the provided callback.
    ///
    /// # Errors
    /// Returns an error if waiting for idle fails, the session lock cannot be
    /// acquired, or compaction itself fails.
    pub async fn compact(
        &self,
        on_event: impl Fn(AgentEvent) + Send + Sync + 'static,
    ) -> Result<()> {
        self.wait_for_idle().await?;
        let cx = AgentCx::for_current_or_request();
        let mut guard = OwnedMutexGuard::lock(Arc::clone(&self.inner.handle), &cx)
            .await
            .map_err(|e| Error::session(format!("session lock failed: {e}")))?;
        guard.compact(on_event).await
    }
}

