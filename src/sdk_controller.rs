async fn run_turn(
    handle: Arc<AsyncMutex<AgentSessionHandle>>,
    abort_slot: Arc<StdMutex<Option<AbortHandle>>>,
    idle: Arc<IdleState>,
    queue: Arc<StdMutex<QueueStore>>,
    text: String,
    images: Vec<ImageContent>,
) {
    let cx = AgentCx::for_current_or_request();

    // Retry settings mirror the RPC path (`run_prompt_with_retry`): network
    // errors are retryable with exponential backoff; context-overflow and auth
    // failures are terminal (see `is_retryable_error`). Aborted turns never
    // retry. Parity gap H3.
    let (max_retries, base_delay_ms, max_delay_ms, retry_enabled) = crate::config::Config::load()
        .map_or((0, 0, 0, false), |c| {
            (
                c.retry_max_retries(),
                c.retry_base_delay_ms(),
                c.retry_max_delay_ms(),
                c.retry_enabled(),
            )
        });

    let mut attempt: u32 = 0;
    loop {
        let (abort_handle, abort_signal) = AbortHandle::new();
        if let Ok(mut slot) = abort_slot.lock() {
            *slot = Some(abort_handle);
        }

        let result = {
            let Ok(mut guard) = OwnedMutexGuard::lock(Arc::clone(&handle), &cx).await else {
                idle.mark_idle();
                return;
            };
            if images.is_empty() {
                guard
                    .prompt_with_abort(text.clone(), abort_signal, |_event| {})
                    .await
            } else {
                let blocks = build_content_blocks(&text, &images);
                guard
                    .session_mut()
                    .run_with_content_with_abort(blocks, Some(abort_signal), |_event| {})
                    .await
            }
        };

        if let Ok(mut slot) = abort_slot.lock() {
            *slot = None;
        }

        // Classify the outcome to decide whether to retry.
        let retryable_error = match &result {
            Ok(message) => {
                if matches!(message.stop_reason, StopReason::Aborted) {
                    // Terminal: never retry an aborted turn.
                    None
                } else if message.stop_reason == StopReason::Error {
                    message.error_message.as_deref().and_then(|err| {
                        crate::error::is_retryable_error(err, Some(message.usage.input), None)
                            .then(|| err.to_string())
                    })
                } else {
                    None
                }
            }
            Err(err) => {
                let err_str = err.to_string();
                crate::error::is_retryable_error(&err_str, None, None).then_some(err_str)
            }
        };

        let Some(error_message) = retryable_error else {
            break;
        };
        if !retry_enabled || attempt >= max_retries {
            break;
        }

        attempt += 1;
        let delay_ms = retry_backoff_ms(base_delay_ms, max_delay_ms, attempt);
        {
            // Surface the retry to subscribers, matching the RPC event stream.
            if let Ok(guard) = OwnedMutexGuard::lock(Arc::clone(&handle), &cx).await {
                guard.emit_event(&AgentEvent::AutoRetryStart {
                    attempt,
                    max_attempts: max_retries,
                    delay_ms: u64::from(delay_ms),
                    error_message,
                });
            }
        }
        asupersync::time::sleep(
            asupersync::time::wall_now(),
            std::time::Duration::from_millis(u64::from(delay_ms)),
        )
        .await;
        if let Ok(guard) = OwnedMutexGuard::lock(Arc::clone(&handle), &cx).await {
            guard.emit_event(&AgentEvent::AutoRetryEnd {
                success: false,
                attempt,
                final_error: None,
            });
        }
    }

    // The AgentEnd subscriber already marks idle + emits a snapshot. Mark idle
    // again defensively in case the turn returned an error before AgentEnd.
    idle.mark_idle();
    let _ = &queue;
}

/// Exponential backoff with a ceiling, matching the RPC `retry_delay_ms` helper.
fn retry_backoff_ms(base_delay_ms: u32, max_delay_ms: u32, attempt: u32) -> u32 {
    let base = u64::from(base_delay_ms);
    let max = u64::from(max_delay_ms);
    let shift = attempt.saturating_sub(1);
    let multiplier = 1u64.checked_shl(shift).unwrap_or(u64::MAX);
    let delay = base.saturating_mul(multiplier).min(max);
    u32::try_from(delay).unwrap_or(u32::MAX)
}

