//! Cloneable, concurrency-safe agent session controller for embedders.
//!
//! [`AgentSessionController`] is the recommended surface for downstream
//! integrators (e.g. Screenpipe) that need to queue follow-ups, steer the
//! active reply, cancel queued prompts, abort, and observe a queue snapshot —
//! all while a prompt turn is in flight.
//!
//! ## Why not `Arc<Mutex<AgentSessionHandle>>`?
//!
//! [`AgentSessionHandle::prompt_with_abort`] takes `&mut self` and is held for
//! the full duration of the turn. If an embedder wraps the handle in a mutex
//! and locks it for the prompt, every concurrent `steer`/`queue_follow_up`/
//! `cancel`/`abort` blocks until the turn ends — exactly when those operations
//! need to be accepted.
//!
//! ## Design
//!
//! The controller separates two lock domains:
//!
//! - The **session** (`Arc<AsyncMutex<AgentSessionHandle>>`) is locked only by
//!   the spawned prompt turn (and by serialized `set_model`/`new_session`,
//!   which first wait for idle). The agent loop drains queued messages through
//!   registered fetchers at its own turn boundaries.
//! - The **queue store + abort slot + idle signal** (`Arc<StdMutex<...>>`) are
//!   touched by every other method. They are never held across an `.await`, so
//!   `steer`/`queue_follow_up`/`cancel`/`snapshot`/`abort`/`wait_for_idle`
//!   never block on the running prompt.
//!
//! Queued messages carry public metadata (stable id, kind, preview, timestamp)
//! and are delivered to the agent via [`Agent::register_message_fetchers`], so
//! Pi's internal turn-drain semantics decide *when* they enter the model turn.
//!
//! `prompt` ACKs immediately and runs the turn on a caller-provided spawner
//! (so `pi_agent_rust` stays runtime-agnostic; Screenpipe passes `tokio::spawn`).

