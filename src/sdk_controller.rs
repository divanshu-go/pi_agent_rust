/// Maximum characters retained for a queued-prompt preview.
const MAX_PREVIEW_CHARS: usize = 200;

/// Stable identifier for a queued message, generated by the SDK.
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize)]
pub struct QueueId(pub String);

impl QueueId {
    #[must_use]
    pub fn as_str(&self) -> &str {
        &self.0
    }
}

impl std::fmt::Display for QueueId {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(&self.0)
    }
}

/// Whether a queued message is steering (interrupts at the next boundary) or a
/// follow-up (delivered when the agent would otherwise go idle).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize)]
#[serde(rename_all = "camelCase")]
pub enum QueueKind {
    Steering,
    FollowUp,
}

/// Public metadata for one queued prompt, suitable for embedder UI.
///
/// The serialized field names match the legacy Screenpipe `pi-queue-changed`
/// contract (`id`, `preview`, `queuedAtMs`) plus an additive `kind`.
#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct QueuedPrompt {
    pub id: QueueId,
    pub kind: QueueKind,
    pub preview: String,
    pub queued_at_ms: u64,
}

/// Snapshot of the controller queue state for embedder UI rendering.
#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct QueueSnapshot {
    pub session_id: Option<String>,
    pub active: bool,
    pub queued: Vec<QueuedPrompt>,
}

/// Ticket returned by [`AgentSessionController::prompt`].
///
/// The turn runs in the background; use [`AgentSessionController::wait_for_idle`]
/// or subscribe to events to observe completion.
#[derive(Debug, Clone)]
pub struct PromptTicket {
    pub id: String,
}

/// Result of an abort operation.
#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct AbortSummary {
    /// Whether an active turn was running and got an abort signal.
    pub aborted_active: bool,
    /// Number of queued messages cleared by this abort.
    pub cleared_queued: usize,
}

