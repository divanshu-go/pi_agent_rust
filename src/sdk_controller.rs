/// Build a user message, consuming `text` and `images`.
fn build_user_message(text: String, images: Vec<ImageContent>) -> Message {
    let timestamp = chrono::Utc::now().timestamp_millis();
    let content = if images.is_empty() {
        UserContent::Text(text)
    } else {
        let mut blocks = Vec::with_capacity(images.len() + 1);
        if !text.trim().is_empty() {
            blocks.push(ContentBlock::Text(TextContent::new(text)));
        }
        for image in images {
            blocks.push(ContentBlock::Image(image));
        }
        UserContent::Blocks(blocks)
    };
    Message::User(UserMessage { content, timestamp })
}

fn build_content_blocks(text: &str, images: &[ImageContent]) -> Vec<ContentBlock> {
    let mut blocks = Vec::new();
    if !text.trim().is_empty() {
        blocks.push(ContentBlock::Text(TextContent::new(text.to_string())));
    }
    for image in images {
        blocks.push(ContentBlock::Image(image.clone()));
    }
    blocks
}

fn make_preview(explicit: Option<String>, text: &str) -> String {
    let source = explicit.unwrap_or_else(|| text.to_string());
    source.chars().take(MAX_PREVIEW_CHARS).collect()
}

fn now_ms() -> u64 {
    u64::try_from(chrono::Utc::now().timestamp_millis().max(0)).unwrap_or(0)
}

