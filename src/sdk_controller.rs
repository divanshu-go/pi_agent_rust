/// Removes a queue snapshot subscription when dropped.
pub struct QueueSubscription {
    queue: Arc<StdMutex<QueueStore>>,
    id: u64,
}

impl Drop for QueueSubscription {
    fn drop(&mut self) {
        if let Ok(mut store) = self.queue.lock() {
            store.snapshot_subscribers.retain(|(id, _)| *id != self.id);
        }
    }
}

#[cfg(test)]
mod idle_guard_tests {
    use super::{IdleGuard, IdleState};
    use std::sync::Arc;

    #[test]
    fn guard_drop_marks_idle_and_wakes_waiters() {
        let idle = Arc::new(IdleState::new());
        idle.mark_active();
        assert!(idle.is_active());
        let mut rx = idle.subscribe_idle().expect("active -> waiter");

        {
            let _guard = IdleGuard {
                idle: Arc::clone(&idle),
            };
            // Guard in scope; still active.
            assert!(idle.is_active());
        } // guard dropped here (simulates spawned-turn unwind/return)

        assert!(!idle.is_active(), "drop must mark idle");
        assert!(
            rx.try_recv().is_ok_and(|v| v.is_some()),
            "waiter must be woken on guard drop"
        );
    }
}

#[cfg(test)]
mod retry_backoff_tests {
    use super::retry_backoff_ms;

    #[test]
    fn first_attempt_uses_base_delay() {
        assert_eq!(retry_backoff_ms(2000, 60_000, 1), 2000);
    }

    #[test]
    fn doubles_each_attempt_until_ceiling() {
        assert_eq!(retry_backoff_ms(2000, 60_000, 2), 4000);
        assert_eq!(retry_backoff_ms(2000, 60_000, 3), 8000);
        assert_eq!(retry_backoff_ms(2000, 60_000, 4), 16_000);
    }

    #[test]
    fn caps_at_max_delay() {
        // attempt 6: 2000 * 2^5 = 64000 > 60000 ceiling.
        assert_eq!(retry_backoff_ms(2000, 60_000, 6), 60_000);
        // huge attempt must not overflow.
        assert_eq!(retry_backoff_ms(2000, 60_000, 64), 60_000);
    }

    #[test]
    fn zero_base_yields_zero() {
        assert_eq!(retry_backoff_ms(0, 60_000, 3), 0);
    }
}
