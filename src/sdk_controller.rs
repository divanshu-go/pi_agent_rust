/// Spawner closure type. The controller hands a boxed future to this; the
/// embedder spawns it on its own runtime (e.g. `tokio::spawn`).
pub type ControllerSpawner = Arc<dyn Fn(BoxFuture<'static, ()>) + Send + Sync + 'static>;

struct ControllerInner {
    handle: Arc<AsyncMutex<AgentSessionHandle>>,
    queue: Arc<StdMutex<QueueStore>>,
    idle: Arc<IdleState>,
    /// Abort handle for the active turn, if any.
    abort_slot: Arc<StdMutex<Option<AbortHandle>>>,
    spawner: ControllerSpawner,
    next_queue_id: AtomicU64,
    next_ticket_id: AtomicU64,
}

