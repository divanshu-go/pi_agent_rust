/// Cloneable, concurrency-safe agent session controller.
///
/// Clone freely and share across async tasks; all clones reference the same
/// underlying session, queue, and abort state.
#[derive(Clone)]
pub struct AgentSessionController {
    inner: Arc<ControllerInner>,
}

/// Create a controller from session options plus a runtime spawner.
///
/// `spawner` runs the background prompt turn. Pass `tokio::spawn` (boxed) when
/// embedding under Tokio:
///
/// ```ignore
/// let spawner = Arc::new(|fut| { tokio::spawn(fut); });
/// let controller = create_agent_session_controller(options, spawner).await?;
/// ```
pub async fn create_agent_session_controller(
    options: SessionOptions,
    spawner: ControllerSpawner,
) -> Result<AgentSessionController> {
    let handle = create_agent_session(options).await?;
    let session_id = handle.state().await.ok().and_then(|s| s.session_id);
    Ok(AgentSessionController::from_handle(
        handle, spawner, session_id,
    ))
}

impl AgentSessionController {
    /// Build a controller around an existing handle (useful for tests and
    /// advanced embedding).
    #[must_use]
    pub fn from_handle(
        mut handle: AgentSessionHandle,
        spawner: ControllerSpawner,
        session_id: Option<String>,
    ) -> Self {
        let queue = Arc::new(StdMutex::new(QueueStore::new(session_id)));

        // Register fetchers that pull from the controller queue store by kind.
        // The agent stores fetched messages into its own internal queue and
        // then applies its configured drain mode, so the controller hands over
        // every matching entry on each fetch and lets the agent decide pacing.
        {
            let steering_queue = Arc::clone(&queue);
            let follow_queue = Arc::clone(&queue);
            let steering_fetcher = move || -> BoxFuture<'static, Vec<Message>> {
                let q = Arc::clone(&steering_queue);
                Box::pin(async move {
                    q.lock()
                        .map(|mut store| store.drain_kind(QueueKind::Steering))
                        .unwrap_or_default()
                })
            };
            let follow_fetcher = move || -> BoxFuture<'static, Vec<Message>> {
                let q = Arc::clone(&follow_queue);
                Box::pin(async move {
                    q.lock()
                        .map(|mut store| store.drain_kind(QueueKind::FollowUp))
                        .unwrap_or_default()
                })
            };
            handle.session_mut().agent.register_message_fetchers(
                Some(Arc::new(steering_fetcher)),
                Some(Arc::new(follow_fetcher)),
            );
        }

        // Bridge agent lifecycle events to the idle signal: the turn is idle
        // once AgentEnd fires. We chain a session-level subscriber.
        let idle = Arc::new(IdleState::new());
        {
            let idle_for_events = Arc::clone(&idle);
            let queue_for_events = Arc::clone(&queue);
            handle.subscribe(move |event: AgentEvent| {
                if matches!(event, AgentEvent::AgentEnd { .. }) {
                    idle_for_events.mark_idle();
                    // Snapshot fires so embedders see active=false promptly.
                    if let Ok(store) = queue_for_events.lock() {
                        let snap = store.snapshot(false);
                        notify_subscribers(&store, &snap);
                    }
                }
            });
        }

        let inner = Arc::new(ControllerInner {
            handle: Arc::new(AsyncMutex::new(handle)),
            queue,
            idle,
            abort_slot: Arc::new(StdMutex::new(None)),
            spawner,
            next_queue_id: AtomicU64::new(0),
            next_ticket_id: AtomicU64::new(0),
        });
        Self { inner }
    }

    fn next_queue_id(&self) -> QueueId {
        let n = self.inner.next_queue_id.fetch_add(1, Ordering::SeqCst);
        QueueId(format!("q-{n}"))
    }

    fn next_ticket_id(&self) -> String {
        let n = self.inner.next_ticket_id.fetch_add(1, Ordering::SeqCst);
        format!("emb-{n}")
    }
}

/// Notify all snapshot subscribers. Called while holding the queue lock; the
/// callbacks must not re-enter controller queue methods synchronously.
fn notify_subscribers(store: &QueueStore, snapshot: &QueueSnapshot) {
    for (_, sub) in &store.snapshot_subscribers {
        sub(snapshot.clone());
    }
}

